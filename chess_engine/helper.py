####################################################################################
# ----------------------------------- CONSTANTS ------------------------------------
####################################################################################
EMP = '--'
ORTHOGONAL = [(-1, 0), (0, 1), (1, 0), (0, -1)]
DIAGONAL = [(-1, 1), (1, 1), (1, -1), (-1, -1)]
KING_MOVES = ORTHOGONAL + DIAGONAL
KNIGHT_MOVES = [(-2, -1), (-2, 1), (-1, -2), (-1, 2), (1, -2), (1, 2), (2, -1), (2, 1)]


####################################################################################
# ------------------------------ BOUND CHECK HELPERS -------------------------------
####################################################################################
def in_bounds(row, col, dimension=8):
    return 0 <= row < dimension and 0 <= col < dimension


####################################################################################
# ------------------------------ PIECE CHECK HELPERS -------------------------------
####################################################################################
def piece_color(piece):
    return None if piece == EMP else piece[0]

def piece_type(piece):
    return None if piece == EMP else piece[1]


####################################################################################
# ------------------------------ COLOR CHECK HELPERS -------------------------------
####################################################################################
def enemy_color(color):
    return 'b' if color == 'w' else 'w'

def turn_color(white_to_move):
    return 'w' if white_to_move else 'b'


####################################################################################
# ----------------------------- PAWN PROMOTION HELPERS -----------------------------
####################################################################################
def get_promotion_row(white_to_move):
    return 0 if white_to_move else 7

def get_promotion_color(white_to_move):
    return turn_color(white_to_move)

def get_promotion_piece(white_to_move, chosen_piece):
    turn = turn_color(white_to_move)
    return turn + chosen_piece